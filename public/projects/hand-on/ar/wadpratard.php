                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;วัดพระบรมธาตุบ้านตาก อยู่ที่ ต.เกาะตะเภา อ.บ้านตาก จ.ตาก ฝั่งตะวันตกของแม่น้ำปิง เดิมวัดแห่งนี้เป็นเมืองตากเก่า ก่อนที่จะมีการย้ายตัวเมืองไปอยู่ที่ ต.ระแหง ตัวเมืองตากในปัจจุบัน ห่างไปทางทิศใต้ประมาณ ๓๐ กิโลเมตร อันมีประวัติยาวนานตั้งแต่สมัย พระนางจามเทวี ล่องเรือเสด็จไปเมืองลำพูน หยุดพักบริเวณ
ณแห่งนี้ พบว่าเป็นเมืองร้าง จึงได้สั่งให้มีการฟื้นฟูบูรณะเมืองแห่งนี้ จนกลายเป็นชุมชนเมืองตาก
วัดพระบรมธาตุบ้านตาก ยังปรากฏในศิลาจารึของพ่อขุนรามคำแหง ที่ทรงกระทำยุทธหัตถี ชนะศึกเจ้าเมืองฉอดบนเนินเขาใกล้กับพระบรมธาตุ ประมาณ ๕๐๐ เมตร
ตามตำนานพระเจ้าเลียบโลก ซึ่งเขียนเป็นภาษาเหนือ กล่าวไว้ว่า สมเด็จพระสัมมาสัมพุทธเจ้าได้เสด็จโปรดสัตว์ล่องมาตามลำน้ำปิง พระองค์ได้เสด็จมายัง ดอยมะหิยังกะ ในเขตตัวเมืองตาก ตรัสกับพระอานนท์ว่าสถานที่นี้เป็นสถานที่สำราญ ร่มรื่น หากเราตถาคตปรินิพพานแล้วให้นำอัฐิและเกศากลับมายังดอยมะหิยังกะ
ภายหลังจากสมเด็จพระสัมมาสัมพุทธเจ้าปรินิพพาน พระอรหันต์ทั้ง ๔ ได้นำพระบรมสาริกธาตุของพระองค์
พร้อมด้วยเกศาอีก ๔ องค์ มาประดิษฐานยังดอยมะหิยังกะ แล้วก่อเจดีย์รูปทรงสี่เหลี่ยมแล้ว นำพระบรม
สารีริกธาตุบรรจุไว้ในพระเจดีย์ เพื่อเป็นที่สักการบูชาของมนุษย์และเทวดาทั้งหลายต่อมา พระครูพิทักษ์พระบรมธาตุ (ทองอยู่) ได้ไปนมัสการพระธาตุชเวดากอง ณ กรุงย่างกุ้ง ประเทศพม่า ท่านจึงได้จำมาก่อสร้างองค์พระเจดีย์ เหมือนเจดีย์ชเวดากองล้อมคลุมองค์เก่าไว้ ปิดทองสวยงาม
